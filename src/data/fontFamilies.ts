// Function to get a random font from a specific style
export function getRandomFontFromStyle(style: string): string {
  const fonts = fontFamiliesByStyle[style];
  if (!fonts || fonts.length === 0) {
    return 'Inter'; // Fallback
  }
  return fonts[Math.floor(Math.random() * fonts.length)];
}

// Function to get random fonts without repetition
export function getRandomFontsFromStyle(style: string, count: number = 1): string[] {
  const fonts = fontFamiliesByStyle[style];
  if (!fonts || fonts.length === 0) {
    return ['Inter']; // Fallback
  }
  
  const shuffled = [...fonts].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, fonts.length));
}